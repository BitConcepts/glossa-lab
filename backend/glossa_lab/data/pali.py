def get_corpus_text() -> str:
    """Return the Pali corpus text."""
    return PALI_TEXT


def get_corpus_symbols() -> list[str]:
    """Return character-level symbol sequence from the Pali corpus."""
    return [c for c in PALI_TEXT.lower() if c.isalpha()]


def get_corpus_inscriptions() -> list[list[str]]:
    """Return word-level character sequences for the Pali corpus.

    Each word becomes a sequence of its constituent characters.
    Words with < 2 characters are excluded. Suitable for CipherConstructor
    and AnchorConvergenceBenchmark.
    """
    seqs = []
    for word in PALI_TEXT.lower().split():
        chars = [c for c in word if c.isalpha()]
