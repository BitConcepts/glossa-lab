# ── Combined South Dravidian corpus ─────────────────────────────────────────

def _get_tamil_text() -> str:
    """Import Tamil text from dravidian.py without circular dependency."""
    try:
        from glossa_lab.data.dravidian import OLD_TAMIL_TEXT  # noqa: PLC0415
        return OLD_TAMIL_TEXT
    except ImportError:
        return ""


def get_corpus_text() -> str:
    """Return the combined South Dravidian corpus (Tamil + Kannada + Telugu)."""
    tamil = _get_tamil_text()
    return f"{tamil} {KANNADA_TEXT} {TELUGU_TEXT}".strip()


def get_corpus_symbols() -> list[str]:
    """Return character-level symbol sequence from the South Dravidian corpus."""
    return [c for c in get_corpus_text().lower() if c.isalpha()]


def get_corpus_inscriptions() -> list[list[str]]:
    """Return word-level character sequences for the South Dravidian corpus.

    Each word in the text becomes a sequence of its constituent characters.
    Words with < 2 characters are excluded. Suitable for CipherConstructor
    and AnchorConvergenceBenchmark.

    Example: 'mane neeru' -> [['m','a','n','e'], ['n','e','e','r','u']]
    """
    text = get_corpus_text().lower()
    seqs = []
    for word in text.split():
        chars = [c for c in word if c.isalpha()]
        if len(chars) >= 2:
            seqs.append(chars)
    return seqs


def get_kannada_symbols() -> list[str]:
    """Return character-level symbols from the Kannada sub-corpus only."""
    return [c for c in KANNADA_TEXT.lower() if c.isalpha()]


def get_telugu_symbols() -> list[str]:
    """Return character-level symbols from the Telugu sub-corpus only."""
    return [c for c in TELUGU_TEXT.lower() if c.isalpha()]
