"""Matched-Frequency Sequence-Information Experiment.

Research question:
  Does the mapping inference signal in the NW Semitic test1 corpus depend on
  true within-word sequential structure, or only on sign unigram frequencies
  and word-length distributions?

Design:
  Four corpus conditions, each matched on unigram frequency and word-length
  distribution, progressively destroying order information:

  C0  Real corpus (ground truth)
      20 independent SA seeds; all structure preserved.

  C1  Within-word shuffle
      For each word, signs are randomly permuted within the word boundary.
      Preserves: global sign frequencies, word lengths, word boundaries.
      Destroys:  within-word bigram/n-gram order, positional marginals (T/I/M).

  C2  Cross-word token shuffle
      All tokens are pooled and randomly re-assigned to word slots, preserving
      each word's length but ignoring which signs appear together.
      Preserves: global sign frequencies, word lengths.
      Destroys:  within-word order, co-occurrence patterns, word identity.

  C3  Frequency-matched random generation
      Words are generated by sampling signs i.i.d. from the empirical unigram
      frequency distribution, with word lengths drawn from the original
      word-length distribution.
      Preserves: sign frequency distribution (approx), word-length distribution.
      Destroys:  all sequential structure, exact per-word frequencies.

  100 independent corpus instances are generated for each of C1–C3, and the
  identical inference pipeline is run on each.  For C0, 20 independent SA seeds
  are run on the single real corpus.  All instances use the Old Hebrew language
  model with identical hyperparameters.

Metrics (per instance/run):
  M1  Mean mapping consistency  — fraction of seeds agreeing on modal assignment
  M2  High-confidence sign count — signs with consistency ≥ 0.75
  M3  Assignment entropy (bits) — distribution of proposed consonant assignments
  M4  Bigram plausibility       — mean bigram log-prob of decoded output under LM
  M5  Intra-instance Hamming    — mean pairwise Hamming distance between N_PAIR
                                  seed pairs (measures solution convergence)

Statistical tests:
  For each metric M and each control Ci, report:
    d       Cohen's d effect size  (C0 mean – Ci mean) / pooled SD
    U_p     Mann-Whitney U empirical p-value (one-tailed, H1: C0 > Ci)
    rank_r  Rank-biserial correlation  r = 1 – 2U/(n0 * ni)

Usage:
    python -m glossa_lab.experiments.fuls_sequence_information_test

Output:
    reports/fuls_sequence_info_<timestamp>.json
"""

